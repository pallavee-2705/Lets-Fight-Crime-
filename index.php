  <h2 style="color:rgb(255,255,255)"><u><i>Why we should file a Complaint?</u></i></h2>
  <p class="note"></p>
  <p>One of the most important things that you, as a resident, can do when you or a neighbour is the victim of any type of crime is: report it! The frequency and severity of occurrences will increase if they are not dealt with. One other important thing is to report suspicious people or activity. You are familiar with your neighbourhood. If someone is snooping around give the police a call. It may just prevent a crime or even an arrest may be made.<p>
<p>The importance of reporting crime and disorder, no matter how big or small, is absolutely vital for the EPS, and for you! There are numerous resources within the EPS that look at crime and disorder, and analyze it for patterns and then send the information out to our front line members so that they can go to the areas that have been targeted by offenders. On a daily basis, this information is provided to the members you see driving or walking in your community.</p>
<p>Each day, our crime analysts review each file that is drawn by the members of Downtown Division, looking for patterns, links, and trends in crimes, including crime types, locations, and methods. From this, target areas are identified, and on numerous occasions the individuals responsible for the crimes are also identified. The final result, although it is not always seen, is often arrests, whether it is in relation to the original crime reported, or for other occurrences.</p>
<p>Each day, the analysts put together a map of the division, which records and illustrates each and every crime that occurs in the division. Further, each report of a disorder issue is also documented in a similar fashion. Each week, the Community Liaison Constables, Community Sergeants, Beat Sergeants, as well as divisional management meet, review, and discuss crime patterns and emerging hotspots that have been identified. From these meetings strategies are discussed and then utilized by the members of Downtown Division to try and curb or solve the issues that have been identified.
</p><br>

