<h3>
<p class="ff">
1.Always treat you fairly with dignity and respect ensuring you have fair access to our services at a time that is reasonable and suitable for you.</li>
2.Provide you with information so you know who your dedicated Neighbourhood Policing Team is, where they are based, how to contact them and how to work with them.</li>
3.Ensure your Neighbourhood Policing Team and other police patrols are visible and on your patch at times when they will be most effective and when you tell us you most need them. We will ensure your team are not taken away from neighbourhood business more than is absolutely necessary. They will spend at least 80% of their time visibly working in your neighbourhood, tackling your priorities. Staff turnover will be minimised.</li>
4.Respond to every message directed to your Neighbourhood Policing Team within 24 hours and, where necessary, provide a more detailed response as soon as we can.</li>
5.Aim to answer 999 calls within 10 seconds, deploying to emergencies immediately giving an estimated time of arrival, getting to you safely, and as quickly as possible. In urban areas, we will aim to get to you within 15 minutes and in rural areas within 20 minutes.</li>
6.Answer all non-emergency calls promptly. If attendance is needed, send a patrol giving you an estimated time of arrival, and:</li>

  *If you are vulnerable or upset aim to be with you within 60 minutes.</li>
  *If you are calling about an issue that we have agreed with your community will be a neighbourhood priority (listed opposite) and attendance is required, we will aim to be with you within 60 minutes.</li>
  *Alternatively, if appropriate, we will make an appointment to see you at a time that fits in with your life and within 48 hours.</li>
  *If agreed that attendance is not necessary we will give you advice, answer your questions and/or put you in touch with someone who can help.</li>

7.Arrange regular public meetings to agree your priorities, at least once a month, giving you a chance to meet your local team with other members of your community. These will include opportunities such as surgeries, street briefings and mobile police station visits which will be arranged to meet local needs and requirements. Your local arrangements can be found opposite.</li>
8.Provide monthly updates on progress, and on local crime and policing issues. This will include the provision of crime maps, information on specific crimes and what happened to those brought to justice, details of what action we and our partners are taking to make your neighbourhood safer and information on how your force is performing.</li>
9.If you have been a victim of crime agree with you how often you would like to be kept informed of progress in your case and for how long. You have the right to be kept informed at least every month if you wish and for as long as is reasonable.</li>
10.Acknowledge any dissatisfaction with the service you have received within 24 hours of reporting it to us. To help us fully resolve the matter, discuss with you how it will be handled, give you an opportunity to talk in person to someone about your concerns and agree with you what will be done about them and how quickly.</li>
